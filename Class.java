/*This is the way class is defined in Java:
	class ClassName {
    		//variables
   		// methods
	} 
*/


class switch {
  // instance variable
  private boolean isOn;
  // method
  public void turnOn() {
    isOn = true;
  }
  // method
  public void turnOff() {
  	isOn = false;
  }
}


//Here, class-"switch"
//      instance variables-"isOn"
//      methods-"turnOn , turnOff"
